#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		cont;
	char		*a;

	a = (char *)s;
	cont = 1;
	while (cont <= n)
	{
		a[cont - 1] = 0;
		cont++;
	}
}
