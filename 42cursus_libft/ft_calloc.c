#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*ptr;

	ptr = malloc(size * count);
	if (!ptr)
		return (0);
	if (!(ft_memset((void *)ptr, 0, size * count)))
		return (0);
	return (ptr);
}
